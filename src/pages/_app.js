import "styles/globals.css";

import PropTypes from "prop-types";
import { Provider } from "next-auth/client";
import Layout from "components/Layout";
import { ThemeProvider, CssBaseline } from "@material-ui/core";
import Head from "next/head";
import theme from "defaultTheme";
import { ApolloProvider } from "@apollo/client";
import { useApollo } from "lib/apollo-client";
import ClientOnly from "components/ClientOnly";
import { useEffect } from "react";
import { useRouter } from "next/dist/client/router";

function MyApp({ Component, pageProps }) {
  const token = pageProps?.session?.token;
  const apolloClient = useApollo(token, pageProps.initialApolloState);
  const router = useRouter();

  useEffect(() => {
    // Remove the server-side injected CSS.
    const jssStyles = document.querySelector("#jss-server-side");
    if (jssStyles) {
      jssStyles.parentElement.removeChild(jssStyles);
    }
  }, []);

  useEffect(() => {
    if (!token && !router.pathname.includes("auth")) {
      router.push("/auth/credentials-signin");
    }
  }, [token]);

  return (
    <Provider
      // Provider options are not required but can be useful in situations where
      // you have a short session maxAge time. Shown here with default values.
      options={{
        // Client Max Age controls how often the useSession in the client should
        // contact the server to sync the session state. Value in seconds.
        // e.g.
        // * 0  - Disabled (always use cache value)
        // * 60 - Sync session state with server if it's older than 60 seconds
        clientMaxAge: 0,
        // Keep Alive tells windows / tabs that are signed in to keep sending
        // a keep alive request (which extends the current session expiry) to
        // prevent sessions in open windows from expiring. Value in seconds.
        //
        // Note: If a session has expired when keep alive is triggered, all open
        // windows / tabs will be updated to reflect the user is signed out.
        keepAlive: 0,
      }}
      session={pageProps.session}
    >
      <ThemeProvider theme={theme}>
        <CssBaseline />
        {token && (
          <>
            <ApolloProvider client={apolloClient}>
              <Head>
                <title>Ortho Panel</title>
                <meta
                  name="description"
                  content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
              </Head>
              <Layout>
                <ClientOnly>
                  <Component {...pageProps} />
                </ClientOnly>
              </Layout>
            </ApolloProvider>
          </>
        )}
        {!token && (
          <ClientOnly>
            <Component {...pageProps} />
          </ClientOnly>
        )}
      </ThemeProvider>
    </Provider>
  );
}

MyApp.propTypes = {
  Component: PropTypes.func,
  pageProps: PropTypes.shape({
    initialApolloState: PropTypes.shape({}),
    session: PropTypes.shape({
      token: PropTypes.string,
    }),
  }),
};

MyApp.defaultProps = {
  Component: null,
  pageProps: {
    initialApolloState: {},
  },
};

export default MyApp;
